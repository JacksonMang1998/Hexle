	<p class="title">What is a hex code?</p>
	<p class="body-text">
		Hex codes are numbers in base 16 and are used to represent colors on the web (and many other places)!<br /><br /> You can represent up to 16,777,216
		unique colors with a simple 6 digit hex code!
	</p>

	<p class="title">How do hex codes work?</p>
	<p>
		Colors are represented using <b>RGB</b> (Red, Green, Blue) values. In a hex code, there are 3 pairs of digits. Each pair represents a particular
		amount of red, green or blue (in that order only!)<br /><br />
	</p>

	<div class="center"><p>#<b style="color: red;">RR</b><b style="color: green;">GG</b><b style="color: blue">BB</b></p></div>
	<br />

	<p>
		Each pair can range from <b>00</b> to <b>FF</b>, representing 256 unique color values. The higher the value, the more of that color will be
